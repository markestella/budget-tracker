// Route patterns for detecting which section the user is in
export const FINANCE_ROUTES = [
  '/dashboard/income',
  '/dashboard/accounts',
  '/dashboard/investments',
  '/dashboard/budgets',
  '/dashboard/expenses',
  '/dashboard/wishlist',
];

export function isFinanceRoute(pathname: string): boolean {
  return FINANCE_ROUTES.some(route => pathname.startsWith(route));
}
